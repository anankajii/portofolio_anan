export default function Dock({ openWindows, onIconClick, onWindowClick }) {
  const dockApps = [
    { id: "projects", icon: <FlutterIconSmall />, label: "Projects" },
    { id: "skills", icon: <PackagesIconSmall />, label: "Skills" },
    { id: "github", icon: <GithubIconSmall />, label: "Github" },
    { id: "resume", icon: <PdfIconSmall />, label: "Resume" },
  ];

  return (
    <div className="fixed bottom-4 left-1/2 -translate-x-1/2 z-50">
      <div className="bg-gray-900/60 backdrop-blur-2xl rounded-2xl px-3 py-2 shadow-2xl border border-white/10">
        <div className="flex items-center gap-2">
          {dockApps.map((app) => {
            const isOpen = openWindows.find(w => w.id === app.id);
            return (
              <button
                key={app.id}
                onClick={() => isOpen ? onWindowClick(app.id) : onIconClick(app.id)}
                className="group relative"
                title={app.label}
              >
                <div className={`hover:scale-110 transition-transform duration-200 ${
                  isOpen ? 'scale-105' : ''
                }`}>
                  {app.icon}
                </div>
                {isOpen && (
                  <div className="absolute -bottom-1 left-1/2 -translate-x-1/2 w-1 h-1 bg-white rounded-full" />
                )}
                <div className="absolute -top-10 left-1/2 -translate-x-1/2 bg-gray-800/90 backdrop-blur-sm text-white text-xs px-2 py-1 rounded opacity-0 group-hover:opacity-100 transition-opacity whitespace-nowrap pointer-events-none">
                  {app.label}
                </div>
              </button>
            );
          })}
        </div>
      </div>
    </div>
  );
}
